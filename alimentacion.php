								<h3>En que esta basada una alimentanción saludable</h3>

								<p> Una alimentación saludable mas que nada esta basada en ingerir alimentos que le aporten al cuerpo los nutrientes y la energia necesaria que este necesita, para poder vivir.<br>La base de una alimentacion saludable se inicia cuando adquirimos la conciencia de lo que implica para nuestro cuerpo. de los alimentosobtenemos la energia y los nutrimentos ue permiten que cada parte de nuestro organismo cumplacon sus funciones.<br>se denomina "dieta" al conjunto de alimentos y platillos que se consumen cada dia para que la dieta sea buena, debe tener ciertas caracteristicas: Ser completa, equilibrada, inocua, suficiente, variada y adecuada.</p>

								<img alt="Cambiar imagen" height="198" onclick="this.src='images/al8.jpg';" onmouseover="this.src='images/al6.jpg';"src="images/al8.jpg">
								<p>Una dieta <span>Completa, </span>se refiere a la cantidad de nutrimentro que contenga. Cada persona, de acuerdo a su edad, genero, composicion corporal y actividad fisica, necesita un cierta cantidad de ellos para que su organismofuncione adecuadamente;<span>equilibrada, </span>se basa en que combine los alimentos correctamente.; <span>inocua, </span> es decir, libre de microorganismos, toxinas y agentes contaminantes que pueden causar enfermedades; <span>suficiente, </span>para que asi aporte la energia adecuada que permieta mantener un peso corporal saludable u en cada una de las estapas de la vida.</p>
