						<h3>Importancia de una alimentación saludable</h3>
						<p>La alimentación es una actividad fundamental en nuestra vida. Por una parte,tiene un papel importantísimo en la socialización del ser humano y su relación con el medio que le rodea. Por otra, es el proceso por el cual obtenemos los nutrientes que nuestro cuerpo necesita para vivir.<br>De igual manera es de suma importancia mantener una alimentacion sana y equilibrada para tener una mejor calidad de vida.</p>

